#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		*(char*)dst = *(const char*)src;
		i++;
		dst++;
		src++;
	}
	return (dst);
}